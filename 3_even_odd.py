def is_odd(number_one):
    if number_one % 2 == 1:
        return True
    elif number_one == float:
        return False
    else:
        return False


print(is_odd(11))


def is_even(number_two):
    if number_two % 2 == 0:
        return True
    elif number_two == float:
        return False
    else:
        return False


print(is_even(10))
